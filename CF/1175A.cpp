#include<bits/stdc++.h>
using namespace std; 
#define mod 1000000007
#define gcd(a,b) __gcd(a,b)
#define lcm(a,b) (a*b)/gcd(a,b)
#define bits(x) __builtin_popcountll(x)


typedef long long int ll;
void solve()
{
    ll cnt=0,n,k;cin>>n>>k;
    while (n!=0 || n>=k)
    {
        if(n%k==0){n=n/k;cnt++;}
        else {cnt+=n%k;n-=n%k;}
    }
    cnt+=max(0LL,n);
    cout<<cnt<<"\n";
}


int main()
{ 
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    ll t;cin>>t;
    // cout<<t<<"Hi test complete\n";
    while(t--)
        {
            solve();
        }


    return 0;
}